inline float table_lookup(float rad, const float table[][2])
{
	if (rad > TWO_PI)
	{
		int n = (int)(rad / TWO_PI);
		rad -= n * TWO_PI;
	}

	int low = 0;
	int high = TABLE_SIZE - 1;
	if (rad < table[0][0])
	{
		return table[0][1];
	}
	else if (rad > table[TABLE_SIZE - 1][0])
	{
		return table[TABLE_SIZE - 1][1];
	}

	while (low <= high)
	{
		int mid = (high + low) / 2;
		if (rad < table[mid][0])
		{
			high = mid - 1;
		}
		else if (rad > table[mid][0])
		{
			low = mid + 1;
		}
		else
		{
			return table[mid][1];
		}
	}

	float low_delta = table[low][0] - rad;
	float high_delta = rad - table[high][0];
	if (low_delta < high_delta)
	{
		return table[low][1];
	}
	else
	{
		return table[high][1];
	}
}
